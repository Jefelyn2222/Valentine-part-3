function openEnvelope() {
    document.querySelector(".envelope-img").style.transform = "rotateX(180deg)";
    document.querySelector(".letter").style.transform = "translateY(-10px)";
    document.querySelector(".letter").style.opacity = "1";
    document.querySelector(".flower").style.opacity = "1";
    document.querySelector(".flower").style.transform = "scale(1)";
}
